//不要求最短的数组--只要从begin到end到一条路径即可
function findLadders(
  beginWord: string,
  endWord: string,
  wordList: string[]
): string[] {
  const map = new Map()
  wordList.push(beginWord)
  const set = new Set(wordList)
  let canEnd = false
  for (const word of wordList) {
    for (let i = 0; i < word.length; i++) {
      for (let j = 97; j < 123; j++) {
        const str =
          word.slice(0, i) + String.fromCodePoint(j) + word.slice(i + 1)
        if (set.has(str) && str !== word) {
          if (str === endWord) {
            canEnd = true
          }
          if (!map.has(word)) {
            map.set(word, [str])
          } else {
            map.get(word).push(str)
          }
        }
      }
    }
  }
  if (!canEnd) return []
  let res: string[] = []
  const de = new Set()
  const dfs = function (temp: string[], begin: string) {
    if (res.length > 0) return
    if (begin === endWord) {
      res = temp.slice()
      return
    }
    de.add(begin)
    for (const item of map.get(begin)) {
      if (de.has(item)) continue
      temp.push(item)
      dfs(temp, item)
      temp.pop()
    }
    //de.delete(begin)
    //不需要回溯--对于当前到单词，如果不能到end=》说明当前单词永远不会被用到
  }
  dfs([beginWord], beginWord)
  return res
}
