function lemonadeChange(bills: number[]): boolean {
  const list = [0, 0, 0]
  for (const num of bills) {
    const del = num - 5
    if (del === 0) {
      list[0]++
    }
    if (del === 5) {
      if (--list[0] < 0) return false
      list[1]++
    }
    if (del === 15) {
      if (list[0] > 0 && list[1] > 0) {
        list[0]--
        list[1]--
      } else if (list[0] > 2) {
        list[0] -= 3
      } else {
        return false
      }
      list[2]++
    }
  }
  return true
};
