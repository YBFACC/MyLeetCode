//自己--Morris遍历空间O(1)时间O(n)
var inorderSuccessor = function (root, p) {
  if (!root) return null
  let curr = root
  while (curr) {
    if (curr.left) {
      let preNode = curr.left
      while (preNode.right && preNode.right !== curr) {
        preNode = preNode.right
      }
      if (!preNode.right) {
        preNode.right = curr
        curr = curr.left
      } else {
        if (curr.right && curr.val === p.val) {
          let temp = curr.right
          while (temp.left && temp.left.val > curr.val && temp.left !== curr) {
            temp = temp.left
          }
          return temp
        }
        curr = curr.right
        preNode.right = null
      }
    } else {
      if (curr.right && curr.val === p.val) {
        let temp = curr.right
        while (temp.left && temp.left.val > curr.val && temp.left !== curr) {
          temp = temp.left
        }
        return temp
      }
      curr = curr.right
    }
  }
  return null
}
